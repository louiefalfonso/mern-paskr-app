export const updateTask = async (req, res) => {
  try {
    const { userId } = req.user;
    const taskId = req.params.id;

    const { title, team, stage, date, priority} = req.body;

    // Find the task to be updated
    const task = await Task.findById(taskId);
    if (!task) {
      return res.status(404).json({ status: false, message: "Task not found" });
    }

    // Update task fields
    task.title = title || task.title;
    task.team = team || task.team;
    task.stage = stage?.toLowerCase() || task.stage;
    task.date = date || task.date;
    task.priority = priority?.toLowerCase() || task.priority;

    // Create activity log for the update
    let text = "Task has been updated";
    if (team?.length > 1) {
      text = text + ` and assigned to ${team?.length - 1} others.`;
    }

    text =
      text +
      ` The task priority is set a ${priority} priority, so check and act accordingly. The task date is ${new Date(
        date
      ).toDateString()}. Thank you!!!`;

    const activity = {
      type: "update",
      activity: text,
      by: userId,
    };

    task.activities.push(activity);

    // Update the task
    await task.save();

    // Create a new notice for the updated task
    await Notice.create({
      team,
      text,
      task: task._id,
    });

    res
      .status(200)
      .json({ status: true, task, message: "Task updated successfully" });
  } catch (error) {
    console.log(error);
    return res.status(400).json({ status: false, message: error.message });
  }
};

export const getTasks = async (req, res) => {
  try {
    const { stage, isTrashed } = req.query;

    let query = { isTrashed: isTrashed ? true : false };

    if (stage) {
      query.stage = stage;
    }

    let queryResult = Task.find(query)
      .populate({
        path: "team",
        select: "name title email",
      })
      .sort({ _id: -1 });

    const tasks = await queryResult;

    res.status(200).json({
      status: true,
      tasks,
    });
  } catch (error) {
    console.log(error);
    return res.status(400).json({ status: false, message: error.message });
  }
};